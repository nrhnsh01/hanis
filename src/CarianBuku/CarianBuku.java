    private void btnCarianActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCarianActionPerformed
    String sqlCari = "SELECT * FROM buku WHERE isbnNo = '"+txtCarian.getText()+"'";

        try {
            rs = st.executeQuery(sqlCari);
            rs.last();
            int a = rs.getRow();

            if(a>0){
                JOptionPane.showMessageDialog(null, "Data Telah Dijumpai");
                txtid.setText(rs.getString(1));
                txtisbnNo.setText(rs.getString(2));
                txtnamaBuku.setText(rs.getString(3));
                txtnamPengarang.setText(rs.getString(4));
                txthargaBuku.setText(rs.getString(5));
            }
            else {
                JOptionPane.showMessageDialog(null, "Tiada Data Dijumpai");
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }    // TODO add your handling code here:
    }//GEN-LAST:event_btnCarianActionPerformed

