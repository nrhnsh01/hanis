    private void btnKemaskiniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKemaskiniActionPerformed
     String sqlEdit = "UPDATE buku SET ID='"+txtid.getText()+"',IsbnNo='"+txtisbnNo.getText()+"',NamaBuku='"+txtnamaBuku.getText()+"',Pengarang='"+txtnamPengarang.getText()+"',HargaBuku='"+txthargaBuku.getText()+"'";

        try {
            int x = st.executeUpdate(sqlEdit);
            if(x>0){
                JOptionPane.showMessageDialog(null, "Berjaya dikemaskini");
            }
            else {
                JOptionPane.showMessageDialog(null, "Tidak berjaya dikemaskini");
            }

        } catch (HeadlessException | SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }// TODO add your handling code here:
    }//GEN-LAST:event_btnKemaskiniActionPerformed

